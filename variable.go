package main

import "fmt"

func main() {
	var name string

	name = "belajar golang dasar"
	fmt.Println(name)

	name = "hello world"
	fmt.Println(name)

	var angka = 3
	fmt.Println(angka)

	angka = 5
	fmt.Println(angka)

	kata := "contoh variable"
	fmt.Println(kata)

	var (
		firstName = "Ali"
		lastName = "Gugus"
	)
	fmt.Println(firstName)
	fmt.Println(lastName)
}